my_string = "Python"
print(my_string)
print(type(my_string))#<class 'str'>

my_int = 1
print(my_int)
print(type(my_int))#<class 'int'>

my_float = 1.0
print(my_float)
print(type(my_float))#<class 'float'>

my_complex = 1 + 2j
print(my_complex)
print(type(my_complex))#<class 'complex'>

my_boolean = True
print(my_boolean)
print(type(my_boolean))#<class 'bool'>

my_list = [1,2,3]
print(my_list)
print(type(my_list))#<class 'list'>

